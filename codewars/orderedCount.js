const orderedCount = function (text) {
    // Создаём объект для хранения количества вхождений каждой буквы
    const letterCounts = {};
    // Создаём массив для хранения порядка появления символов
    const order = [];

    // Проходим по каждому символу в тексте
    for (const char of text) {
        // Если символ уже есть в объекте, увеличиваем его значение на 1
        if (letterCounts[char]) {
            letterCounts[char]++;
        } else {
            // Иначе добавляем символ в объект со значением 1
            letterCounts[char] = 1;
            // И добавляем символ в массив order для сохранения порядка появления
            order.push(char);
        }
    }

    // Создаём массив для результата в нужном формате
    return order.map(char => [char, letterCounts[char]]);
}

//Подсчитайте количество вхождений каждого символа и верните его в виде массива
// в порядке появления. Для пустого вывода возвращается пустой список.

orderedCount("abracadabra")  //[['a', 5], ['b', 2], ['r', 2], ['c', 1], ['d', 1]]