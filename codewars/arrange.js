function arrange(s) {
    const t = [];
    const n = s.length;
    let reverse = false;

    for (let i = 0; i < n / 2; i++) {
        if (!reverse) {
            t.push(s[i]);            // Добавляем элемент с начала
            if (n - i - 1 !== i) {
                t.push(s[n - i - 1]); // Добавляем элемент с конца
            }
        } else {
            t.push(s[n - i - 1]);     // Добавляем элемент с конца
            if (n - i - 1 !== i) {
                t.push(s[i]);          // Добавляем элемент с начала
            }
        }
        reverse = !reverse; // Переворачиваем порядок добавления
    }

    return t;
}
arrange([9, 7, -2, 8, 5, -3, 6, 5, 1]) //[9, 1, 5, 7, -2, 6, -3, 8, 5]
